const createHandler = async (req, res) => {
  // get the data from the request body
  const { montant, etat, typeCarte, monnaie,paymentIntentId } = req.body;

  const payment = await createDBPayment({
    montant,
    etat, 
    typeCarte,
    monnaie,
    paymentIntentId,
  });
  if (!payment) {
    return res.status(400).json({
      status: "Bad Request",
      message: "Error while creating a new payment in database",
    });
  }

  return res.status(201).json({
    status: "success",
    message: "Payment created successfully",
    data: payment,
  });
};

const updateHandler = async (req, res) => {
  // get the id from the request params
  const { id } = req.params;
  const valideId = validateId(id);
  // get the data from the request body
  const { etat } = req.body;
  // call the service to update the payment
  const payment = await updatePayment(valideId, etat);
  if (!payment) {
    return res.status(400).json({
      status: "Bad Request",
      message: "Error while updating payment",
    });
  }
  return res.status(200).json({
    status: "OK",
    message: "payment updated successfully",
    data: payment,
  });
};

